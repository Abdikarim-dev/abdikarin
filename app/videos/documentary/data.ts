// Helper function to get videos by category
export function getVideosByCategory(category: string) {
  if (category === "all") {
    return documentaryVideos
  }
  return documentaryVideos.filter((video) => video.categories.includes(category))
}

// Helper function to search videos
export function searchVideos(query: string) {
  const searchTerm = query.toLowerCase()
  return documentaryVideos.filter(
    (video) =>
      video.title.toLowerCase().includes(searchTerm) ||
      video.description.toLowerCase().includes(searchTerm) ||
      video.channel.toLowerCase().includes(searchTerm),
  )
}

// Helper function to get a video by ID
export function getVideoById(id: number) {
  return documentaryVideos.find((video) => video.id === id)
}

// Helper function to get related videos
export function getRelatedVideos(currentVideoId: number, limit = 3) {
  const currentVideo = getVideoById(currentVideoId)
  if (!currentVideo) return []

  return documentaryVideos
    .filter(
      (video) => video.id !== currentVideoId && video.categories.some((cat) => currentVideo.categories.includes(cat)),
    )
    .slice(0, limit)
}

